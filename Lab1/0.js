function pow(x, n) { //пробіл між аргументами
  let result = 1; //пробіли навколо "="

  for (let i = 0; i < n; i++) { //пробіли
    result *= x; //зміст дужок на окремому рядку
  }

  return result;
}

let x = prompt("x?", "");
let n = prompt("n?", ""); //змінні на окремих рядках

if (n < 0) {
  alert(`Степень ${n} не поддерживается,
    введите целую степень, большую 0`); //великий рядок розбито
} else {
  alert( pow(x, n) );
}